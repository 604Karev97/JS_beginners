function sum(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function mult(a, b) {
    return a * b;
}

function div(a, b) {
    return a / b;
}

let a = 9;
let b = 4;

console.log(`Сложение: ${sum(a, b)}`);
console.log(`Вычитание: ${sub(a, b)}`);
console.log(`Умножение: ${mult(a, b)}`);
console.log(`Деление: ${div(a, b)}`);